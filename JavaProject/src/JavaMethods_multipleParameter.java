public class JavaMethods_multipleParameter {
    static void nameandAge(String name, int age){
        System.out.println(name + " is " + age);
    }

        public static void main(String[] args) {
            nameandAge("Liam", 18);
            nameandAge("Elon", 28);
            nameandAge("Mark", 30);
        }
    
    
}
